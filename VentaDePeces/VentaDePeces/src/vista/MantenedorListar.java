    private void jButtonListarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonListarActionPerformed
        // TODO add your handling code here:
        
        if(jRadioButtonPezTropical.isSelected()==true){
            jTextAreaVisor.setText("Listado de peces tropicales\n"+
                    "| ID | Especie | Precio | Stock | Color\n\n");
            for(Pez obj: PezDAO.obtenerPezTropical()){
                jTextAreaVisor.append(obj.toString()+"\n");
            }
            
        }
        
        if(jRadioButtonPezDorado.isSelected()==true){
            jTextAreaVisor.setText("Listado de peces dorados\n"+
                    "| ID | Especie | Precio | Stock | Tama√±o\n\n");
            for(Pez obj: PezDAO.obtenerPezDorado()){
                jTextAreaVisor.append(obj.toString()+"\n");
            }
        }
        
        if(jRadioButtonPezKoi.isSelected()==true){
            jTextAreaVisor.setText("Listado de peces koi\n"+
                    "| ID | Especie | Precio | Stock | Edad |\n\n");
            for(Pez obj: PezDAO.obtenerPezKoi()){
                jTextAreaVisor.append(obj.toString()+"\n");
            }
        }
        
        if(jRadioButtonTodos.isSelected()==true){
            jTextAreaVisor.setText("");
            for(Pez obj: PezDAO.obtenerDatos()){
                jTextAreaVisor.append(obj.toString()+"\n");
        }
        }
    }//GEN-LAST:event_jButtonListarActionPerformed

    
    
